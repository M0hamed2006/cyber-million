// ==================== دالة الحصول على الأسئلة بذكاء ====================

function getSmartQuestion(questionType, difficulty) {
  try {
    if (!QUESTIONS_DATABASE[questionType]) {
      console.warn(`❌ نوع السؤال '${questionType}' غير موجود`);
      return null;
    }

    const questions = QUESTIONS_DATABASE[questionType][difficulty] || 
                     QUESTIONS_DATABASE[questionType]['medium'] || [];

    if (questions.length === 0) {
      console.warn(`❌ لا توجد أسئلة للمستوى '${difficulty}'`);
      return null;
    }

    // اختر سؤال عشوائي
    const randomIndex = Math.floor(Math.random() * questions.length);
    return questions[randomIndex];
  } catch (error) {
    console.error('❌ خطأ في جلب السؤال:', error);
    return null;
  }
}

